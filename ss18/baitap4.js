let key = Number(prompt("mời bạn nhập vào 1 số từ 0 -> 9 "));
switch (key) {
    case 0:
        console.log("số không");
        break;
    case 1:
        console.log("số một");
        break;
    case 2:
        console.log("số hai");
        break;
    case 3:
        console.log("số ba");
        break;
    case 4:
        console.log("số bốn");
        break;
    case 5:
        console.log("số năm");
        break;
    case 6:
        console.log("số sáu");
        break;
    case 7:
        console.log("số bảy");
        break;
    case 8:
        console.log("số tám");
        break;
    case 9:
        console.log("số chín");
        break;
    default:
        break;
}